package PlaBas.FleetManager;

public class calculator {

	
	public float somma(float a, float b) {
		return a+b;
	}
	
	public float sottrazione(float a, float b) {
		return a-b;
	}
	
	public float prodotto(float a, float b) {
		return a*b;
	}

	public float divisione(float a, float b) {
		return a/b;
	}
}
